export function normalizeLocale(locale) {
  if (!locale) {
    return DEFAULT_LOCALE;
  }

  const normalized = String(locale).toLowerCase();
  return SUPPORTED_LOCALES.includes(normalized) ? normalized : DEFAULT_LOCALE;
}

export function getLocaleConfig(locale) {
  return LOCALE_CONFIG[normalizeLocale(locale)];
}

export function getLocalizedPath(locale, pageKey = "home") {
  const normalizedLocale = normalizeLocale(locale);
  const segment = PAGE_SEGMENTS[pageKey] ?? "";
  return segment ? `/${normalizedLocale}/${segment}` : `/${normalizedLocale}`;
}

export function detectPreferredLocale() {
  if (typeof window === "undefined") {
    return DEFAULT_LOCALE;
  }

  const browserLocales = navigator.languages?.length
    ? navigator.languages
    : [navigator.language];

  for (const browserLocale of browserLocales) {
    if (!browserLocale) {
      continue;
    }

    const lowerLocale = browserLocale.toLowerCase();
    const region = lowerLocale.split("-")[1]?.toUpperCase();

    if (region === "MX") {
      return "es-mx";
    }

    if (region === "US") {
      return "en-us";
    }

    if (lowerLocale.startsWith("es")) {
      return "es-mx";
    }

    if (lowerLocale.startsWith("en")) {
      return "en-us";
    }
  }

  const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone ?? "";

  if (timeZone.includes("Mexico")) {
    return "es-mx";
  }

  return DEFAULT_LOCALE;
}

export function buildAbsoluteUrl(path) {
  const base = import.meta.env.BASE_URL ?? SITE_BASE_PATH;
  const normalizedBase = base.endsWith("/") ? base : `${base}/`;
  const normalizedPath = path.replace(/^\/+/, "");
  return new URL(normalizedPath, `${SITE_ORIGIN}${normalizedBase}`).toString();
}

export function getSeoForRoute(route) {
  const locale = normalizeLocale(route.meta?.locale);
  const pageKey = route.meta?.pageKey ?? "home";
  const localeConfig = getLocaleConfig(locale);
  const path = getLocalizedPath(locale, pageKey);
  const pageMeta = localeConfig.seo.pageMeta?.[pageKey];
  const shouldIndex = pageKey !== "thankYou";

  const title =
    pageKey === "home"
      ? localeConfig.seo.homeTitle
      : pageMeta?.title ?? localeConfig.seo.defaultTitle;

  const description =
    pageKey === "home"
      ? localeConfig.seo.homeDescription
      : pageMeta?.description ?? localeConfig.seo.defaultDescription;

  const alternates = SUPPORTED_LOCALES.map((supportedLocale) => ({
    hreflang: getLocaleConfig(supportedLocale).hreflang,
    href: buildAbsoluteUrl(getLocalizedPath(supportedLocale, pageKey)),
  }));

  alternates.push({
    hreflang: "x-default",
    href: buildAbsoluteUrl(`/${DEFAULT_LOCALE}`),
  });

  return {
    title,
    description,
    canonical: buildAbsoluteUrl(path),
    alternates,
    htmlLang: localeConfig.htmlLang,
    ogLocale: localeConfig.ogLocale,
    siteName: localeConfig.seo.siteName,
    robots: shouldIndex
      ? "index, follow, max-image-preview:large"
      : "noindex, nofollow, max-image-preview:large",
  };
}
